//WAP to encrypt a grade by adding 8 to it .Decrypt it to show the correct grade
public class akarshak04 {
    public static void main(String[] args) {
        char grade = 'B';
        grade = (char) (grade + 8);
        System.out.println(grade);
        grade = (char) (grade - 8);
        System.out.println(grade + "");
        System.out.println("akarshak");
    }
}